#include<bits/stdc++.h>
using namespace std;
int main(){
    int N,M;
    cin>>N>>M;
    for(int i=0;i<N;i++) // একটি লুপ চলছে ০ থেকে N পর্যন্ত -> কমপ্লেক্সিটি O(N)

 {

cout<<"hello"<<endl;

 }

 for(int i=0;i<M;i++) // একটি লুপ চলছে ০ থেকে M পর্যন্ত -> কমপ্লেক্সিটি O(M)

 {

cout<<"hello"<<endl;

 }

 // টোটাল কমপ্লেক্সিটি O(N) + O(M) [ কারন দুটি লুপ আলাদাভাবে চলছে ]
    return 0;
}