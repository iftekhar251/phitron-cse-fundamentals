#include <stdio.h>
#include <math.h>
int main()
{

    float n1 = sqrt(20);
    printf("%lf\n", n1);

    int n2 = abs(-20);
    printf("%d\n", n2);

    int n3 = round(5.7);
    printf("%d\n", n3);

    int n4 = ceil(4.6);
    printf("%d\n", n4);
    
    int n5 = floor(4.6);
    printf("%d\n", n5);
    return 0;
}